/**
 * Returns the average of two numbers.
 *
 * @category arithmetic
 *
 * @example
 * This is the code sample:
 * ```ts
 * function getAverage(x, y) {
 *   return (x + y) / 2.0;
 * }
 * ```
 */

const getAverage = (x: number, y: number) => {
    return (x + y) / 2.0;
}

/**
 * Returns the sum of two numbers.
 *
 * @category arithmetic
 *
 * @example
 * This is the code sample:
 * ```ts
 * function getSum(x, y) {
 *   return x+y
 * }
 * ```
 */

const getSum = (x: number, y: number) => {
    return x + y;
}

